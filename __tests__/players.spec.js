jest.mock('../src/Http');

import Vainglory from '../src/Vainglory';

const validMethods = ['single'];

describe('Vainglory.single', () => {
  const vainglory = new Vainglory('aaa.bbb.ccc');

  it('should contain valid methods', () => {
    validMethods.forEach((method) => {
      expect(vainglory.players[method]).toBeDefined();
    })
  });

  it('.single should get a single player', async () => {
    const playerId = '6abb30de-7cb8-11e4-8bd3-06eb725f8a76';
    const player = await vainglory.players.single(playerId);

    expect(player).toMatchSnapshot();
  });

  it('.single should throw if invalid param is given', async () => {
    try {
      await vainglory.players.single({});
    } catch (err) {
      expect(err).toThrow();
    }
  });

});
